package wrapperclasses;

public class WrapperClassExample {
    public static void main(String[] args) {
        int a = 10;//primitive value.
        Integer i = a;//primitive value assign to wrapper class.//autoboxing
        System.out.println(i);

//automatic converting primitive into wrapper class this process is called as autoboxing.

        int b = i;// opposite of autoboxing
    }}