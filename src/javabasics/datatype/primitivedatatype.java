package javabasics.datatype;

public class primitivedatatype {
    public static void main(String[] args) {
        //integer data types//
        byte b=127;// byte data type range(-128 to 127)
        short s=32767; //short data type range(-32768 to 32767)
        int a=50;
        long l=65767798;//long data type
        //floating data types//
        float f=3.14f; // we have to use complsary 'f' as sufix //
        double d=23345.4465865; //sufix is not must hare//
        //boolean data type//
        boolean b1=true;
        //char data types//
        char c='r';
        System.out.println("@@@@ Integer data types @@@@");
        System.out.println(b);
        System.out.println(s);
        System.out.println(a);
        System.out.println(l);

        System.out.println("#### float data types ####");
        System.out.println(f);
        System.out.println(d);

        System.out.println("$$$$ boolean data types $$$$");
        System.out.println(b1);

        System.out.println("**** char data types ****");
        System.out.println(c);


    }
}
