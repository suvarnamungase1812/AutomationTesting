package literalsexample.operator.relational;

public class RelationalcompariosnExample {
    public static void main(String[] args) {


        int a = 250;
        int b = 350;

        System.out.println(a < b);//true
        System.out.println(a>b);//false
        System.out.println(a<=b);//true
        System.out.println(a>=b);//false
    }
}
