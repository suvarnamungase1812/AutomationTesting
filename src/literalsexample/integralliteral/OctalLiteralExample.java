package literalsexample.integralliteral;
public class OctalLiteralExample
{
    public static void main(String[] args) {
        int  a= 777;
        int b = 0754;
        //octal literals allow digit 0 to 7 only.
        //and base is 8.
        System.out.println(a);
        System.out.println(b);
        //0754 is converted octal to decimal


            }
}
